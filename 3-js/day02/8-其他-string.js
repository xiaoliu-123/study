//toString()   String()  +''
var a = 123;
console.log(a.toString());  //'123'
console.log(String(a));   //'123'
console.log(a+'');    //'123'

console.log(a.toString(2));  //a转为2进制字符串输出
console.log(a.toString(8));  //8进制字符串
console.log(a.toString(16)); //16进制字符串




